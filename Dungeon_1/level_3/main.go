package main

import (
	"fmt"
)

func main() {
	// Declare variable format: var <variable_name> <data_type>
	var a int = 10
	var b string = "Hello, World!"
	var c float64 = 3.14
	var d bool = true
	// Print time
	fmt.Println("a:", a)
	fmt.Println("b:", b)
	fmt.Println("c:", c)
	fmt.Println("d:", d)
}